package com.http.request;

/**
 * The enum Http method.
 *
 * @author WilliamAhmed
 * @since 11 /09/2018
 */
public enum HttpRequestMethod {

	/**
	 * Get http method.
	 */
	GET,
	/**
	 * Post http method.
	 */
	POST,
	/**
	 * Put http method.
	 */
	PUT,
	/**
	 * Delete http method.
	 */
	DELETE,
	/**
	 * Head http method.
	 */
	HEAD,
	/**
	 * Trace http method.
	 */
	TRACE,
	/**
	 * Options http method.
	 */
	OPTIONS,
	/**
	 * Connect http method.
	 */
	CONNECT,
	/**
	 * Patch http method.
	 */
	PATCH;
}
